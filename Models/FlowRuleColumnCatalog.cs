using System.IO;
using System.Text.Json;

namespace SpeedEmulator.Models;

public enum FlowRuleColumnKind
{
    Reference,
    Const
}

public static class FlowRuleColumnCatalog
{
    private static readonly JsonSerializerOptions JsonOptions = new()
    {
        PropertyNameCaseInsensitive = true
    };

    private static readonly Lazy<Dictionary<string, FlowRuleColumns>> ParsedColumns = new(ParseColumns);

    public static bool TryGetColumns(string bankName, FlowRuleColumnKind kind, out IReadOnlyList<string> columns)
    {
        if (ParsedColumns.Value.TryGetValue(bankName, out var ruleColumns))
        {
            var configuredColumns = kind == FlowRuleColumnKind.Reference
                ? ruleColumns.Reference
                : ruleColumns.Const;

            if (configuredColumns.Count > 0)
            {
                columns = configuredColumns;
                return true;
            }
        }

        columns = [];
        return false;
    }

    private static Dictionary<string, FlowRuleColumns> ParseColumns()
    {
        var result = ParseRawColumns();

        foreach (var item in ParsePackagedColumns())
        {
            result[item.Key] = item.Value;
        }

        return result;
    }

    private static Dictionary<string, FlowRuleColumns> ParsePackagedColumns()
    {
        var path = Path.Combine(AppContext.BaseDirectory, "Data", "zhencheng-flow-rule-columns.json");
        if (!File.Exists(path))
        {
            return [];
        }

        try
        {
            using var stream = File.OpenRead(path);
            var document = JsonSerializer.Deserialize<FlowRuleColumnDocument>(stream, JsonOptions);
            return document?.Banks ?? [];
        }
        catch
        {
            return [];
        }
    }

    private static Dictionary<string, FlowRuleColumns> ParseRawColumns()
    {
        var result = new Dictionary<string, FlowRuleColumns>(StringComparer.OrdinalIgnoreCase);
        string? currentBank = null;
        FlowRuleColumnKind? currentKind = null;

        foreach (var rawLine in RawColumns.Split(['\r', '\n'], StringSplitOptions.RemoveEmptyEntries))
        {
            var line = rawLine.Trim();
            if (string.IsNullOrWhiteSpace(line))
            {
                continue;
            }

            if (IsReferenceHeading(line))
            {
                currentKind = FlowRuleColumnKind.Reference;
                continue;
            }

            if (IsConstHeading(line))
            {
                currentKind = FlowRuleColumnKind.Const;
                continue;
            }

            if (!line.Contains('、'))
            {
                currentBank = line;
                result.TryAdd(currentBank, new FlowRuleColumns());
                currentKind = null;
                continue;
            }

            if (currentBank is null)
            {
                continue;
            }

            var columns = line
                .Split('、', StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries)
                .ToList();

            var target = result[currentBank];
            var kind = currentKind
                ?? (target.Reference.Count == 0 ? FlowRuleColumnKind.Reference : FlowRuleColumnKind.Const);

            if (kind == FlowRuleColumnKind.Reference)
            {
                target.Reference = columns;
            }
            else
            {
                target.Const = columns;
            }

            currentKind = null;
        }

        return result;
    }

    private static bool IsReferenceHeading(string line)
    {
        return string.Equals(line, "参照明细", StringComparison.OrdinalIgnoreCase);
    }

    private static bool IsConstHeading(string line)
    {
        return line.StartsWith("固定", StringComparison.OrdinalIgnoreCase);
    }

    private sealed class FlowRuleColumns
    {
        public List<string> Reference { get; set; } = [];

        public List<string> Const { get; set; } = [];
    }

    private sealed class FlowRuleColumnDocument
    {
        public Dictionary<string, FlowRuleColumns> Banks { get; set; } = new(StringComparer.OrdinalIgnoreCase);
    }

